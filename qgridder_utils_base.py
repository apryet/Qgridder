def get_spatial_indexes(all_layers) : 
    """
    Description
    ----------
    Get spatial indexes

    Parameters
    ----------
    all_layers : list of Qgis vector layer
 
    Returns
    -------
    list of Qgis spatial indexes

    Examples
    --------
    >>> 
    """

    spatial_indexes = []
    for v_layer in all_layers : 
        v_layerIndex = QgsSpatialIndex()
